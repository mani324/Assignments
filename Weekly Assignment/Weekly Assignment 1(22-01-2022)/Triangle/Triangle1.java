package javawork;
public class Triangle1 {
	public static void main(String[] args) {
		int i,j;
		int numberOfRows=5;
		for(i=0;i<=numberOfRows;i++)//outer loop(rows)
		{
		for(j=0;j<=i;j++)//inner loop (columns)
		System.out.print("@");//prints @
			{
	    System.out.println("");//create some space and move to next line
			}
			
		}
			
	}

}
